	SELECT     FI_CODIGO, '* Placas Mx. Vacias' 
	FROM       FACTIMP
	WHERE   (FI_TRAC_MX = '' OR FI_TRAC_MX IS NULL) AND (FACTIMP.FI_CODIGO = @FI_CODIGO)


