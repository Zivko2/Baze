	                      MEDIDA ON FACTIMPDET.ME_CODIGO = MEDIDA.ME_CODIGO
	WHERE     (FACTIMPDET.FID_NOMBRE IS NULL OR FACTIMPDET.FID_NOMBRE='' OR FACTIMPDET.FID_NOMBRE='TEMP' OR FACTIMPDET.FID_NOMBRE='.') 
	and (FACTIMP.FI_CODIGO = @FI_CODIGO)


