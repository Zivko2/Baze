		@CFV_GRUPOGEN=CFV_GRUPOGEN,  -- ya
		@CFV_PESONETO=CFV_PESONETO, 
		@CFV_PESOBRUTO=CFV_PESOBRUTO, 
		@CFV_ORDCOMPRA=CFV_ORDCOMPRA, 
		@CFV_ORDTRABAJO=CFV_ORDTRABAJO, 
		@CFV_NOENTRADA=CFV_NOENTRADA
