	INSERT INTO FACTIMPADVERTENCIA(FI_CODIGO, FI_COMENTARIO)
	SELECT     FI_CODIGO, '* Placas US. Vacias' 
	FROM       FACTIMP
	WHERE   (FI_TRAC_US = '' OR FI_TRAC_US IS NULL) AND (FACTIMP.FI_CODIGO = @FI_CODIGO)

