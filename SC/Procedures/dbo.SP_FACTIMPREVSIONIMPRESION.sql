	INSERT INTO FACTIMPADVERTENCIA(FI_CODIGO, FI_COMENTARIO)
	SELECT     FACTIMP.FI_CODIGO, '* Registro en Detalle Sin Descripcion en espa¤ol, No. Parte: ' + FACTIMPDET.FID_NOPARTE + ' Cantidad: ' + CONVERT(varchar(50), 
	                      FACTIMPDET.FID_CANT_ST) + ' ' + MEDIDA.ME_CORTO 
	FROM         FACTIMP INNER JOIN
