		                      dbo.MEDIDA MEDIDA_1 ON dbo.FACTIMPDET.ME_CODIGO = MEDIDA_1.ME_CODIGO 
		WHERE   (FACTIMPDET.FID_CANT_ST =0 OR FACTIMPDET.FID_CANT_ST IS NULL) 
		and (FACTIMP.FI_CODIGO = @FI_CODIGO)


	
	end




