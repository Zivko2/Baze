	SELECT     FI_CODIGO, '* Medio de Transporte Vacio' 
	FROM       FACTIMP
	WHERE   (MT_CODIGO = 0 OR MT_CODIGO IS NULL) AND (FACTIMP.FI_CODIGO = @FI_CODIGO)

