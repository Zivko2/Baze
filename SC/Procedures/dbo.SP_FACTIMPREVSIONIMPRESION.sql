	SELECT     FI_CODIGO, '* No. Pedimento Consoliado Vacio' 
	FROM       FACTIMP
	WHERE   (FC_CODIGO = 0 OR FC_CODIGO IS NULL) AND (FACTIMP.FI_CODIGO = @FI_CODIGO) 
