	FROM       FACTEXP
	WHERE   (FE_CONSECUTIVOPED = '' OR FE_CONSECUTIVOPED IS NULL) AND (FACTEXP.FE_CODIGO = @FE_CODIGO) 
	AND CP_CODIGO IN (SELECT CP_CODIGO FROM CLAVEPED WHERE CP_CONSOLIDADO='S')


