	SELECT     FE_CODIGO, '* No. Pedimento Consolidado Vacio' 
	FROM       FACTEXP
	WHERE   (FC_CODIGO = 0 OR FC_CODIGO IS NULL) AND (FACTEXP.FE_CODIGO = @FE_CODIGO) 
	AND CP_CODIGO IN (SELECT CP_CODIGO FROM CLAVEPED WHERE CP_CONSOLIDADO='S')


