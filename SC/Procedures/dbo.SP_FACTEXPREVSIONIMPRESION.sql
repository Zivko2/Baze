	INSERT INTO FACTEXPADVERTENCIA(FE_CODIGO, FE_COMENTARIO)
	SELECT     FE_CODIGO, '* Medio de Transporte Vacio' 
	FROM       FACTEXP
	WHERE   (MT_COMPANY1 = 0 OR MT_COMPANY1 IS NULL) AND (FACTEXP.FE_CODIGO = @FE_CODIGO)

