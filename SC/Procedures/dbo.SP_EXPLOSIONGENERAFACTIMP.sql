CREATE PROCEDURE [dbo].[SP_EXPLOSIONGENERAFACTIMP] @tipofactura char(1), @FE_CODIGO int     as

SET NOCOUNT ON 
DECLARE @CF_EXPLOSIONAUX CHAR(1)


	IF NOT EXISTS(SELECT * FROM VEXPLOSIONFACTEXP WHERE FE_CODIGO=@FE_CODIGO)
	exec SP_EXPLOSIONA_FACTEXP @tipofactura, @FE_CODIGO


SELECT     @CF_EXPLOSIONAUX=CF_EXPLOSIONAUX
FROM         CONFIGURACION


	IF @CF_EXPLOSIONAUX='N'
		Exec SP_EXPLOSIONGENERAFACTIMPNOAUX @tipofactura, @FE_CODIGO
	else
		Exec SP_EXPLOSIONGENERAFACTIMPAUX @tipofactura, @FE_CODIGO









































GO
