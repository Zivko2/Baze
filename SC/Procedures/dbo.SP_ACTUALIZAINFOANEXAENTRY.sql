CREATE PROCEDURE [SP_ACTUALIZAINFOANEXAENTRY] (@et_codigo int)   as

SET NOCOUNT ON 

	UPDATE FACTEXPDET
	SET     FACTEXPDET.AR_IMPFO= isnull(ENTRYSUMDET.AR_CODIGO,0), 
		FACTEXPDET.FED_RATEIMPFO= isnull(ENTRYSUMDET.ETD_RATE,-1), 
		FACTEXPDET.EQ_IMPFO= isnull(ENTRYSUMDET.EQ_IMPFO,1),
		FACTEXPDET.FED_NAFTA= isnull(ENTRYSUMDET.ETD_NAFTA,'N'),
		FACTEXPDET.AR_ORIG= isnull(ENTRYSUMDET.AR_ORIG,0),
		FACTEXPDET.AR_NG_EMP= isnull(ENTRYSUMDET.AR_NG_EMP,0)
	FROM         ENTRYSUMDET INNER JOIN
	                      FACTEXPDET ON ENTRYSUMDET.FED_INDICED = FACTEXPDET.FED_INDICED
	WHERE     (ENTRYSUMDET.ET_CODIGO = @et_codigo)



















