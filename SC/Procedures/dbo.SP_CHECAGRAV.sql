CREATE PROCEDURE dbo.SP_CHECAGRAV ( @CODIGO INTEGER, @SALIDA INTEGER OUTPUT) AS     
SET NOCOUNT ON 

BEGIN
   SELECT @SALIDA = COUNT(*) FROM CONFIGURATIPO WHERE (CFT_TIPO = 'P' OR CFT_TIPO = 'S' )
  AND TI_CODIGO IN (SELECT TI_CODIGO FROM RELTEMBTIPO WHERE TQ_CODIGO = @CODIGO)
END





GO
