CREATE trigger Del_ListaExp on dbo.LISTAEXP for DELETE as
SET NOCOUNT ON 
begin

  /* Se borra el detalle de la lista */
  IF EXISTS (SELECT * FROM ListaExpDet ,deleted WHERE ListaExpDet.Le_Codigo = deleted.Le_Codigo)
     DELETE ListaExpDet FROM ListaExpDet ,deleted WHERE ListaExpDet.Le_Codigo = deleted.Le_Codigo
end

































































