CREATE TRIGGER [DEL_IMPORTSPEC] ON dbo.IMPORTSPEC 
FOR DELETE 
AS
BEGIN

	DELETE FROM IMPORTSPECDET WHERE IMS_CODIGO IN (SELECT IMS_CODIGO FROM DELETED)
	DELETE FROM IMPORTSPECSELECT WHERE IMS_CODIGO IN (SELECT IMS_CODIGO FROM DELETED)

END































