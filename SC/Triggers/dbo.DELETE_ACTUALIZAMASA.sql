CREATE TRIGGER [DELETE_ACTUALIZAMASA] ON dbo.ACTUALIZAMASA 
FOR DELETE 
AS
  IF EXISTS (SELECT * FROM ACTUALIZAMASADET, Deleted  WHERE  ACTUALIZAMASADET.AM_CODIGO = Deleted.AM_CODIGO)
     DELETE ACTUALIZAMASADET FROM ACTUALIZAMASADET, Deleted  WHERE ACTUALIZAMASADET.AM_CODIGO = Deleted.AM_CODIGO

































GO
