CREATE PROCEDURE [dbo].[SP_GENERAQRYPERMISO]   as



SELECT     MIN(PERMISO.PE_PERMISO) AS permiso, max(dbo.PERMISODET.MA_GENERICO)
FROM         PERMISODET RIGHT OUTER JOIN
                      PERMISO ON PERMISODET.PE_CODIGO = PERMISO.PE_CODIGO LEFT OUTER JOIN
                      IDENTIFICA ON PERMISO.IDE_CODIGO = IDENTIFICA.IDE_CODIGO
HAVING      MIN(PERMISO.PE_PERMISO) IS NOT NULL









GO
