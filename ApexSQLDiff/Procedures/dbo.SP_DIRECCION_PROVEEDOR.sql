CREATE PROCEDURE dbo.SP_DIRECCION_PROVEEDOR (@PROVEEDOR INT)   as

SET NOCOUNT ON 
BEGIN
	SELECT DIR_PROVEEDOR.DI_INDICE, PAIS.PA_NOMBRE, PAIS.PA_NAME,
	DIR_PROVEEDOR.DI_CALLE, DIR_PROVEEDOR.DI_NOEXT, DIR_PROVEEDOR.DI_NOINT,
	DIR_PROVEEDOR.DI_COL, DIR_PROVEEDOR.DI_CIUDAD, DIR_PROVEEDOR.DI_MUNIC, 
	ESTADO.ES_NOMBRE, ESTADO.ES_NAME, DIR_PROVEEDOR.DI_CP, 
	DIR_PROVEEDOR.DI_TEL1, DIR_PROVEEDOR.DI_FAX, DIR_PROVEEDOR.PA_CODIGO
	FROM DIR_PROVEEDOR
	LEFT OUTER JOIN
	PAIS ON
	DIR_PROVEEDOR.PA_CODIGO = PAIS.PA_CODIGO
	LEFT OUTER JOIN
	ESTADO ON
	DIR_PROVEEDOR.ES_CODIGO = ESTADO.ES_CODIGO
	WHERE PR_CODIGO = @PROVEEDOR
END
















































