CREATE PROCEDURE dbo.SP_GENERATE_EXHIBIT_BC(@CS_CODIGO INT)    as

SET NOCOUNT ON 
BEGIN
  BEGIN TRAN
  DECLARE @ERRORES INT
SET @ERRORES = 0
  EXEC SP_EXHIBITB_247 @CS_CODIGO;
  EXEC SP_EXHIBITC_247 @CS_CODIGO;
  EXEC SP_EXHIBITCENTRY_247 @CS_CODIGO;

  IF (@@ERROR >0 ) SET @ERRORES =1
  COMMIT TRAN
IF @ERRORES = 0
  RETURN(0)

ELSE BEGIN
  ROLLBACK TRAN
  RETURN(1)

END
END




GO
