CREATE PROCEDURE dbo.SP_GETUMARANCEL (@ARA INTEGER,  @UMARA INTEGER OUTPUT) AS   


BEGIN


  SELECT @UMARA = ME_CODIGO
  FROM ARANCEL
  WHERE AR_CODIGO = @ARA

  IF @UMARA IS NULL SET @UMARA = 0
END



























GO
