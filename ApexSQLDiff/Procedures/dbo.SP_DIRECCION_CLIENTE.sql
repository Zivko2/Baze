CREATE PROCEDURE dbo.SP_DIRECCION_CLIENTE (@CLIENTE INT)   as

SET NOCOUNT ON 
BEGIN
	SELECT DIR_CLIENTE.DI_INDICE, PAIS.PA_NOMBRE, PAIS.PA_NAME,
	DIR_CLIENTE.DI_CALLE, DIR_CLIENTE.DI_NOEXT, DIR_CLIENTE.DI_NOINT,
	DIR_CLIENTE.DI_COL, DIR_CLIENTE.DI_CIUDAD, DIR_CLIENTE.DI_MUNIC, 
	ESTADO.ES_NOMBRE, ESTADO.ES_NAME, DIR_CLIENTE.DI_CP, 
	DIR_CLIENTE.DI_TEL1, DIR_CLIENTE.DI_FAX, DIR_CLIENTE.PA_CODIGO
	FROM DIR_CLIENTE
	LEFT OUTER JOIN
	PAIS ON
	DIR_CLIENTE.PA_CODIGO = PAIS.PA_CODIGO
	LEFT OUTER JOIN
	ESTADO ON
	DIR_CLIENTE.ES_CODIGO = ESTADO.ES_CODIGO
	WHERE CL_CODIGO = @CLIENTE
END
















































