CREATE VIEW dbo.VFORMATAG
with encryption as
SELECT IMT_TABLA,IMT_NUMFORMA FROM IMPORTTABLES
UNION
SELECT IMR_TABLA,IMT_NUMFORMA = CASE 
WHEN IMR_TIPO IN ('A','D') THEN 
(SELECT IMT_NUMFORMA FROM IMPORTTABLES WHERE IMT_TABLA = IMR_TMASTER) 
WHEN IMR_TIPO = 'C' THEN (SELECT IMT_NUMFORMA FROM IMPORTTABLES WHERE IMT_TABLA IN (SELECT ITDC2.IMR_TMASTER FROM IMPORTTABLESDETCONT ITDC2
WHERE ITDC2.IMR_TABLA = IMPORTTABLESDETCONT.IMR_TMASTER) )
ELSE 0  END 
FROM IMPORTTABLESDETCONT





























































