CREATE VIEW dbo.VTEMBARQUE
with encryption as
SELECT TEMBARQUE.TQ_CODIGO, TEMBARQUE.TQ_NOMBRE, TEMBARQUE.TQ_NAME, CONFIGURATEMBARQUE.CFQ_TIPO
FROM TEMBARQUE
LEFT OUTER JOIN CONFIGURATEMBARQUE ON
TEMBARQUE.TQ_CODIGO = CONFIGURATEMBARQUE.TQ_CODIGO











GO
