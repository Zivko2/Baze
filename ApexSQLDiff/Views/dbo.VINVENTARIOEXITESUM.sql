CREATE VIEW dbo.VINVENTARIOEXITESUM
with encryption as
SELECT 
  SUM(ENTSALALMSALDO.END_SALDOALM) AS END_SALDOALM,
  ENTSALALMSALDO.ALM_DESTINO,
  ENTSALALMDET.MA_CODIGO
FROM
  ENTSALALMDET
  INNER JOIN ENTSALALMSALDO ON (ENTSALALMDET.END_INDICED = ENTSALALMSALDO.END_INDICED)
GROUP BY ALM_DESTINO, MA_CODIGO
HAVING      (SUM(END_SALDOALM) > 0) AND (ALM_DESTINO <> 0)


GO
