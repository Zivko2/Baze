CREATE VIEW dbo.VRECARGOF4
with encryption as
SELECT     dbo.VPAGOCONTRIBPERIODOF4.PI_CODIGO, SUM(ISNULL(dbo.RECARGO.REC_TASA, 0)) AS SUMATASA
FROM         dbo.RECARGO RIGHT OUTER JOIN
                      dbo.VPAGOCONTRIBPERIODOF4 ON dbo.RECARGO.REC_FECINI <= dbo.VPAGOCONTRIBPERIODOF4.PI_FEC_PAG AND 
                      dbo.RECARGO.REC_FECINI >= dbo.VPAGOCONTRIBPERIODOF4.PI_FEC_ENTpi
GROUP BY dbo.VPAGOCONTRIBPERIODOF4.PI_CODIGO












































































































