CREATE VIEW dbo.VEXEDESCARGAS
with encryption as
SELECT  dbo.FACTEXP.FE_SEL,dbo.FACTEXP.FE_CODIGO, dbo.FACTEXP.FE_FOLIO, dbo.FACTEXP.FE_FECHA, dbo.TFACTURA.TF_NOMBRE, dbo.TEMBARQUE.TQ_NOMBRE, 
'PATENTE_FOLIO'=case when dbo.PEDIMP.PI_TIPO in ('C', 'A') then dbo.AGENCIAPATENTE.AGT_PATENTE collate database_default + '-' + dbo.PEDIMP.PI_FOLIO collate database_default else
	dbo.PEDIMP.PI_FOLIO end, dbo.FACTEXP.FE_FECHADESCARGA, dbo.CONFIGURATFACT.CFF_TIPODESCARGA, dbo.FACTEXP.FE_DESCARGADA,
	dbo.FACTEXP.FE_DESCITALICA, dbo.FACTEXP.FE_CANCELADO
FROM         dbo.FACTEXP LEFT OUTER JOIN
             dbo.TFACTURA ON dbo.FACTEXP.TF_CODIGO = dbo.TFACTURA.TF_CODIGO LEFT OUTER JOIN
             dbo.CONFIGURATFACT ON dbo.FACTEXP.TF_CODIGO = dbo.CONFIGURATFACT.TF_CODIGO LEFT OUTER JOIN
	     dbo.TEMBARQUE ON dbo.TEMBARQUE.TQ_CODIGO = dbo.FACTEXP.TQ_CODIGO LEFT OUTER JOIN
	     dbo.PEDIMP ON dbo.PEDIMP.PI_CODIGO = dbo.FACTEXP.PI_CODIGO LEFT OUTER JOIN
  	dbo.AGENCIAPATENTE ON dbo.AGENCIAPATENTE.AGT_CODIGO = dbo.PEDIMP.AGT_CODIGO
























































































































































