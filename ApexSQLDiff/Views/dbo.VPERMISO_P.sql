CREATE VIEW dbo.VPERMISO_P
with encryption as
SELECT PE_CODIGO, 'PE_PERMISO'=CASE WHEN  dbo.PERMISO.PE_ANIO IS NULL OR dbo.PERMISO.PE_ANIO='' THEN dbo.PERMISO.PE_PERMISO
ELSE CONVERT(VARCHAR(5),dbo.PERMISO.PE_ANIO)+'-'+dbo.PERMISO.PE_PERMISO collate database_default END, PE_FOLIO
FROM PERMISO
WHERE PE_TIPO = 'P'























































