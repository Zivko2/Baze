CREATE VIEW dbo.VEXPLOSIONFACTEXPTOTNOAUX
with encryption as
SELECT     dbo.FACTEXPDET.FE_CODIGO, SUM(dbo.BOM_DESCTEMP.BST_INCORPOR * dbo.FACTEXPDET.FED_CANT) AS CANTIDAD, 
                      SUM(dbo.BOM_DESCTEMP.BST_INCORPOR * dbo.FACTEXPDET.FED_CANT * VMAESTROCOST.MA_COSTO) AS COSTOTOTAL
FROM         dbo.MAESTRO RIGHT OUTER JOIN
                      dbo.BOM_DESCTEMP ON dbo.MAESTRO.MA_CODIGO = dbo.BOM_DESCTEMP.BST_HIJO RIGHT OUTER JOIN
                      dbo.FACTEXPDET ON dbo.BOM_DESCTEMP.FED_INDICED = dbo.FACTEXPDET.FED_INDICED LEFT OUTER JOIN
		VMAESTROCOST ON dbo.MAESTRO.MA_CODIGO=VMAESTROCOST.MA_CODIGO
WHERE     (dbo.BOM_DESCTEMP.BST_DISCH = 'S') AND
((dbo.BOM_DESCTEMP.TI_CODIGO <> 'S' AND dbo.BOM_DESCTEMP.TI_CODIGO <> 'P' AND dbo.BOM_DESCTEMP.MA_TIP_ENS<>'C') OR
(dbo.BOM_DESCTEMP.TI_CODIGO = 'S' AND dbo.BOM_DESCTEMP.MA_TIP_ENS='C')) AND
(dbo.MAESTRO.TI_CODIGO <> 1 AND dbo.MAESTRO.TI_CODIGO <> 2)
GROUP BY dbo.FACTEXPDET.FE_CODIGO




































































GO
