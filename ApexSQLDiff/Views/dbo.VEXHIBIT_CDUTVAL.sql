CREATE VIEW dbo.VEXHIBIT_CDUTVAL
with encryption as
SELECT VEXHIBIT_B_BASE247.CS_CODIGO, 
    VEXHIBIT_B_BASE247.ETA_RATE, 
    SUM(VEXHIBIT_B_BASE247.ETA_WOMPF) AS ETA_WOMPF, 
    SUM(VEXHIBIT_B_BASE247.ETA_WMPF) AS ETA_WMPF, 
    COSTSUBA.CSA_DUTVALUE, 
    MAX(VEXHIBIT_B_BASE247.MA_NAFTA) AS MA_NAFTA, ET_CODIGO, ET_ENTRY_NO
FROM VEXHIBIT_B_BASE247 LEFT OUTER JOIN
    COSTSUBA ON 
    VEXHIBIT_B_BASE247.CS_CODIGO = COSTSUBA.CS_CODIGO
GROUP BY VEXHIBIT_B_BASE247.CS_CODIGO, 
    VEXHIBIT_B_BASE247.ETA_RATE, COSTSUBA.CSA_DUTVALUE, ET_CODIGO, ET_ENTRY_NO





















































































