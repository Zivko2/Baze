CREATE VIEW dbo.VINFOFISINPI
with encryption as
SELECT     COUNT(FI_CODIGO) AS COUNTFI_CODIGO, FI_TIPO
FROM         dbo.FACTIMP
WHERE     (FI_CANCELADO = 'N') AND (PI_CODIGO <= 0 OR
                      PI_CODIGO IS NULL) AND (FI_TIPO <> 'T')
GROUP BY FI_TIPO







































































