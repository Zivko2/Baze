CREATE VIEW dbo.VEMPAQUE
with encryption as
SELECT     dbo.MAESTRO.MA_CODIGO, dbo.MAESTRO.MA_NOPARTE, dbo.MAESTRO.MA_INV_GEN, dbo.MAESTRO.TI_CODIGO, dbo.MAESTRO.MA_NOPARTEAUX, 
                      dbo.MAESTRO.MA_GENERA_EMP, dbo.MAESTRO.MA_PESO_KG
FROM         dbo.MAESTRO LEFT OUTER JOIN
                      dbo.CONFIGURATIPO ON dbo.MAESTRO.TI_CODIGO = dbo.CONFIGURATIPO.TI_CODIGO
WHERE     (dbo.CONFIGURATIPO.CFT_TIPO = 'E')


































































