CREATE VIEW dbo.[VINFOFESINDESC]
with encryption as
SELECT COUNT(FE_CODIGO) AS COUNTFE_CODIGO, 
    FE_TIPO
FROM FACTEXP
GROUP BY FE_TIPO, FE_DESCARGADA
HAVING (FE_TIPO <> 'T') AND (FE_DESCARGADA = 'N')






































































