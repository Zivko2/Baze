CREATE VIEW dbo.VFACTEXPSUMCOSTOT
with encryption as
SELECT     SUM(dbo.FACTEXPDET.FED_COS_TOT) AS FED_COS_TOT, PI_CODIGO=CASE WHEN dbo.FACTEXP.PI_RECTIFICA<>-1 THEN dbo.FACTEXP.PI_RECTIFICA
	ELSE dbo.FACTEXP.PI_CODIGO END 
FROM         dbo.FACTEXPDET INNER JOIN
                      dbo.FACTEXP ON dbo.FACTEXPDET.FE_CODIGO = dbo.FACTEXP.FE_CODIGO
GROUP BY dbo.FACTEXP.PI_CODIGO, dbo.FACTEXP.PI_RECTIFICA



GO
