CREATE VIEW dbo.VBOM_DESCTEMPGen
with encryption as
SELECT     TOP 100 PERCENT dbo.VBOM_DESCTEMP1.FE_CODIGO, dbo.VBOM_DESCTEMP1.FED_INDICED, SUM(dbo.VBOM_DESCTEMP1.CANTDESC) 
                      AS CANTDESC, dbo.VBOM_DESCTEMP1.BST_TIPODESC, dbo.VBOM_DESCTEMP1.MA_TIP_ENS, dbo.MAESTRO.MA_GENERICO
FROM         dbo.VBOM_DESCTEMP1 INNER JOIN
                      dbo.MAESTRO ON dbo.VBOM_DESCTEMP1.BST_HIJO = dbo.MAESTRO.MA_CODIGO
GROUP BY dbo.VBOM_DESCTEMP1.FE_CODIGO, dbo.VBOM_DESCTEMP1.FED_INDICED, dbo.VBOM_DESCTEMP1.BST_TIPODESC, 
                      dbo.VBOM_DESCTEMP1.MA_TIP_ENS, dbo.MAESTRO.MA_GENERICO
ORDER BY dbo.VBOM_DESCTEMP1.FED_INDICED, dbo.MAESTRO.MA_GENERICO















































