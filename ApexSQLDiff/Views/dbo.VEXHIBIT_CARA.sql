CREATE VIEW dbo.VEXHIBIT_CARA
with encryption as
SELECT VEXHIBIT_B_BASE247.CS_CODIGO, 
    VEXHIBIT_B_BASE247.ETA_RATE, 
    ISNULL(VEXHIBIT_B_BASE247.SPI_CODIGO, 11) 
    AS SPI_CODIGO, ARANCEL.AR_FRACCION, 
    PAIS.PA_CORTO, ET_CODIGO, ET_ENTRY_NO
FROM PAIS RIGHT OUTER JOIN
    VEXHIBIT_B_BASE247 ON 
    PAIS.PA_CODIGO = VEXHIBIT_B_BASE247.PA_CODIGO LEFT OUTER
     JOIN
    ARANCEL ON 
    VEXHIBIT_B_BASE247.AR_CODIGO = ARANCEL.AR_CODIGO
GROUP BY VEXHIBIT_B_BASE247.ETA_RATE, 
    ARANCEL.AR_FRACCION, VEXHIBIT_B_BASE247.CS_CODIGO, 
    PAIS.PA_CORTO, ISNULL(VEXHIBIT_B_BASE247.SPI_CODIGO, 11), ET_CODIGO, ET_ENTRY_NO




































































