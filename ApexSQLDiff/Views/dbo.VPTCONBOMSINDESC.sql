CREATE VIEW dbo.VPTCONBOMSINDESC
with encryption as
SELECT     FACTEXP.FE_FECHA, KARDESPED.KAP_INDICED_PED, KARDESPED.MA_HIJO, KARDESPED.KAP_CANTDESC, FACTEXPDET.FED_NOPARTE, 
                      MAESTRO.MA_NOPARTE, FACTEXPDET.FED_INDICED, FACTEXP.FE_FOLIO
FROM         dbo.FACTEXP FACTEXP LEFT OUTER JOIN
                      dbo.KARDESPED KARDESPED ON FACTEXP.FE_CODIGO = KARDESPED.KAP_FACTRANS LEFT OUTER JOIN
                      dbo.MAESTRO MAESTRO ON KARDESPED.MA_HIJO = MAESTRO.MA_CODIGO LEFT OUTER JOIN
                      dbo.FACTEXPDET FACTEXPDET ON KARDESPED.KAP_INDICED_FACT = FACTEXPDET.FED_INDICED
WHERE     (KARDESPED.MA_HIJO <> 0) AND (KARDESPED.KAP_CANTDESC = 0) AND 
                      (FACTEXPDET.FED_INDICED NOT IN
                          (SELECT     KAP_INDICED_FACT
                            FROM          kardesped
                            WHERE      KARDESPED.[KAP_CANTDESC] > 0))
































GO
