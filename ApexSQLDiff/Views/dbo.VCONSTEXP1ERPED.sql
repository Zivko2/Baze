CREATE VIEW dbo.VCONSTEXP1ERPED
with encryption as
SELECT FACTIMPPED.FI_CODIGO, MIN(FACTIMPPED.FIP_INDICEP) 
    AS FIP_INDICEP
FROM FACTIMP LEFT OUTER JOIN
    FACTIMPPED ON 
    FACTIMP.FI_CODIGO = FACTIMPPED.FI_CODIGO
GROUP BY FACTIMPPED.FI_CODIGO, 
    FACTIMPPED.FIP_TIPO
HAVING (FACTIMPPED.FIP_TIPO = 'P')

























































































































































































































































