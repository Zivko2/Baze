CREATE VIEW dbo.VPEDIDONOIVA
with encryption as
SELECT     SUM(ISNULL(PDC_CONTRIBTOTUS, 0)) AS PDC_CONTRIBTOTUS, PD_CODIGO
FROM         dbo.PEDIDOCONTRIBUCION
WHERE     (CON_CODIGO not in(select con_codigo from contribucion where con_clave='3'))
GROUP BY PD_CODIGO


GO
