CREATE VIEW dbo.VSECTORARA
with encryption as
SELECT SECTORARA.AR_CODIGO, SECTORARA.SA_CODIGO, 
    SECTORARA.SE_CODIGO, SECTOR.SE_DESC, SECTOR.SE_CLAVE
FROM SECTORARA LEFT OUTER JOIN
    SECTOR ON SECTORARA.SE_CODIGO = SECTOR.SE_CODIGO












































































