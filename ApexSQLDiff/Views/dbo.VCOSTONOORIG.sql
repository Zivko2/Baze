CREATE VIEW dbo.VCOSTONOORIG
with encryption as
SELECT     BST_PT, SUM(BST_MATNOORIG) AS COSTO, COUNT(BST_HIJO) AS COUNTNOORIG
FROM         dbo.CLASIFICATLC
WHERE     (BST_TIPOORIG = 'N')
GROUP BY BST_PT


































































