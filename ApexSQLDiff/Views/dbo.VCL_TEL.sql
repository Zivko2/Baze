CREATE VIEW dbo.VCL_TEL
with encryption as
SELECT CLIENTE.CL_CODIGO, DIR_CLIENTE.DI_TEL1, 
    DIR_CLIENTE.DI_TEL2, DIR_CLIENTE.DI_FAX, 
    DIR_CLIENTE.DI_CIUDAD, DIR_CLIENTE.ES_CODIGO, 
    DIR_CLIENTE.PA_CODIGO
FROM CLIENTE LEFT OUTER JOIN
    DIR_CLIENTE ON 
    CLIENTE.CL_CODIGO = DIR_CLIENTE.CL_CODIGO






































































