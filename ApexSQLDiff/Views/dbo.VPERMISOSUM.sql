CREATE VIEW dbo.VPERMISOSUM
with encryption as
SELECT     dbo.PERMISO.PE_CODIGO, dbo.PERMISO.PE_PERMISO, dbo.PERMISO.PE_COMPLE, dbo.PERMISO.PE_FOLIO, dbo.PERMISO.PE_FECHA, 
                      SUM(dbo.PERMISODET.PED_CANT) AS PED_CANT, SUM(dbo.PERMISODET.PED_SALDO) AS PED_SALDO
FROM         dbo.PERMISO LEFT OUTER JOIN
                      dbo.PERMISODET ON dbo.PERMISO.PE_CODIGO = dbo.PERMISODET.PE_CODIGO
GROUP BY dbo.PERMISO.PE_CODIGO, dbo.PERMISO.PE_PERMISO, dbo.PERMISO.PE_COMPLE, dbo.PERMISO.PE_FOLIO, dbo.PERMISO.PE_FECHA























































GO
