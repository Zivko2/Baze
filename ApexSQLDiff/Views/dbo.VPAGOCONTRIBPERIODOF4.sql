CREATE VIEW dbo.VPAGOCONTRIBPERIODOF4
with encryption as
SELECT     MIN(PEDIMP_1.PI_FEC_PAG) AS PI_FEC_ENTpi, PEDIMPF4.PI_FEC_PAG, PEDIMPF4.PI_CODIGO
FROM         dbo.FACTEXP INNER JOIN
                      dbo.PEDIMPDET PEDIMPDET_1 INNER JOIN
                      dbo.KARDESPED ON PEDIMPDET_1.PID_INDICED = dbo.KARDESPED.KAP_INDICED_PED INNER JOIN
                      dbo.PEDIMP PEDIMP_1 ON PEDIMPDET_1.PI_CODIGO = PEDIMP_1.PI_CODIGO ON 
                      dbo.FACTEXP.FE_CODIGO = dbo.KARDESPED.KAP_FACTRANS INNER JOIN
                      dbo.PEDIMP AS PEDIMPF4 ON dbo.FACTEXP.PI_CODIGO = PEDIMPF4.PI_CODIGO
GROUP BY PEDIMPF4.PI_FEC_PAG, PEDIMPF4.PI_CODIGO








GO
