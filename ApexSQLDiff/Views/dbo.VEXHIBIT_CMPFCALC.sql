CREATE VIEW dbo.VEXHIBIT_CMPFCALC
with encryption as
SELECT COSTSUBPER.CS_CODIGO, 
    VEXHIBIT_C247TOTMPF.TOT_MPF / (VEXHIBIT_C247TOTMPF.TOT_MPF
     - 25 * VEXHIBIT_C247W25.[NO.ENTRI/W25] + 21 * VEXHIBIT_C247W21.[NO.ENTRI/W21]
     + VEXHIBIT_C247MAXMPF.MAX_MPF_PAG * VEXHIBIT_C247COUNTMAX.COUNT_ET_CODIGO)
     AS MPFCALC, VEXHIBIT_C247TOTMPF.TOT_MPF
FROM VEXHIBIT_C247W25 RIGHT OUTER JOIN
    COSTSUBPER ON 
    VEXHIBIT_C247W25.CS_CODIGO = COSTSUBPER.CS_CODIGO LEFT
     OUTER JOIN
    VEXHIBIT_C247TOTMPF ON 
    COSTSUBPER.CS_CODIGO = VEXHIBIT_C247TOTMPF.CS_CODIGO
     LEFT OUTER JOIN
    VEXHIBIT_C247W21 ON 
    COSTSUBPER.CS_CODIGO = VEXHIBIT_C247W21.CS_CODIGO LEFT
     OUTER JOIN
    VEXHIBIT_C247COUNTMAX ON 
    COSTSUBPER.CS_CODIGO = VEXHIBIT_C247COUNTMAX.CS_CODIGO
     LEFT OUTER JOIN
    VEXHIBIT_C247MAXMPF ON 
    COSTSUBPER.CS_CODIGO = VEXHIBIT_C247MAXMPF.CS_CODIGO



































































