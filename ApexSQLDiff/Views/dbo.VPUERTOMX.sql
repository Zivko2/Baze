CREATE VIEW dbo.VPUERTOMX
with encryption as
SELECT     TOP 100 PERCENT dbo.PUERTO.PU_CODIGO, dbo.PUERTO.PU_NOMBRE, dbo.PUERTO.PU_CLA_USA
FROM         dbo.CONFIGURACION INNER JOIN
                      dbo.PUERTO ON dbo.CONFIGURACION.CF_PAIS_MX = dbo.PUERTO.PA_CODIGO
ORDER BY dbo.PUERTO.PU_CLA_USA, dbo.PUERTO.PU_NOMBRE






























































