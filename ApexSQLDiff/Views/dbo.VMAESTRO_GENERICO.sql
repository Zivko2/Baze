CREATE VIEW dbo.VMAESTRO_GENERICO
with encryption as
SELECT     MA_CODIGO, MA_NOPARTE, TI_CODIGO, MA_NOMBRE, MA_NAME, ME_COM, MA_NOPARTEAUX, AR_IMPMX, AR_IMPFO, AR_EXPMX, AR_RETRA, 
                      AR_DESP, AR_IMPFOUSA, AR_EXPFO
FROM         dbo.MAESTRO
WHERE     (MA_INV_GEN = 'G') AND (MA_EST_MAT = 'A') AND (MA_OCULTO = 'N')




























































