CREATE VIEW dbo.VESTADO
with encryption as
SELECT ESTADO.ES_CODIGO, ESTADO.ES_CORTO, 
    PAIS.PA_NOMBRE, PAIS.PA_NAME, ESTADO.ES_NOMBRE, 
    ESTADO.ES_NAME
FROM ESTADO INNER JOIN
    PAIS ON ESTADO.PA_CODIGO = PAIS.PA_CODIGO






































































