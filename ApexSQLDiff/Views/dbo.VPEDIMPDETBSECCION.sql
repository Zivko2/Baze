CREATE VIEW dbo.VPEDIMPDETBSECCION
with encryption as
SELECT     PIB_INDICEB, PI_CODIGO,
(SELECT     COUNT(*)
FROM         PEDIMPDETIDENTIFICA
WHERE     PIB_INDICEB = PEDIMPDETB.PIB_INDICEB) AS IDENTIFICA,
(SELECT     COUNT(*)
FROM         PEDIMPDETPERM
WHERE     PIB_INDICEB = PEDIMPDETB.PIB_INDICEB) AS PERMISO,
(SELECT     COUNT(*)
FROM         PEDIMPDETCUENTA
WHERE     PIB_INDICEB = PEDIMPDETB.PIB_INDICEB) AS CUENTA
FROM         dbo.PEDIMPDETB













































