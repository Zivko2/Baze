CREATE VIEW dbo.VKAP_SALDO_PED
with encryption as
SELECT     KAP_CODIGO, PEDIMPDET.PID_CAN_GEN-(SELECT SUM(KARDESPEDSUM.KAP_CANTDESC)
		FROM         KARDESPED KARDESPEDSUM
		WHERE     KARDESPEDSUM.KAP_CODIGO <= KARDESPED.KAP_CODIGO
		AND KARDESPEDSUM.KAP_INDICED_PED = KARDESPED.KAP_INDICED_PED) AS KAP_SALDO_PED
FROM         KARDESPED LEFT OUTER JOIN PEDIMPDET
ON KARDESPED.KAP_INDICED_PED=PEDIMPDET.PID_INDICED


GO
