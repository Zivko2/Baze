CREATE VIEW dbo.VCOSTOTOTALGIUS
with encryption as
SELECT     dbo.FACTEXPDET.FE_CODIGO, SUM(dbo.FACTEXPDET.FED_GRA_GI * dbo.FACTEXPDET.FED_CANT) AS COSTOTOTALGIUS
FROM         dbo.MAESTRO RIGHT OUTER JOIN
                      dbo.FACTEXPDET ON dbo.MAESTRO.MA_CODIGO = dbo.FACTEXPDET.MA_EMPAQUE
GROUP BY dbo.FACTEXPDET.FE_CODIGO



































































