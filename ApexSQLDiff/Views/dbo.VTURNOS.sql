CREATE VIEW dbo.VTURNOS
with encryption as
SELECT     dbo.PERSONAL.TUR_CODIGO
FROM         dbo.PRODUC LEFT OUTER JOIN
                      dbo.PERSONAL ON dbo.PRODUC.US_CODIGO = dbo.PERSONAL.US_CODIGO
GROUP BY dbo.PERSONAL.TUR_CODIGO

GO
