CREATE VIEW dbo.VFACTIMPSUMCOSTOT
with encryption as
SELECT     SUM(dbo.FACTIMPDET.FID_COS_TOT) AS FID_COS_TOT, PI_CODIGO=CASE WHEN dbo.FACTIMP.PI_RECTIFICA<>-1 THEN dbo.FACTIMP.PI_RECTIFICA
	ELSE dbo.FACTIMP.PI_CODIGO END 
FROM         dbo.FACTIMPDET INNER JOIN
                      dbo.FACTIMP ON dbo.FACTIMPDET.FI_CODIGO = dbo.FACTIMP.FI_CODIGO
GROUP BY dbo.FACTIMP.PI_CODIGO, dbo.FACTIMP.PI_RECTIFICA































































GO
