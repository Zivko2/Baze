CREATE VIEW dbo.VPUERTOM
with encryption as
SELECT PUERTO.PU_CODIGO, PUERTO.PU_NOMBRE, 
    PAIS.PA_NOMBRE, PAIS.PA_NAME
FROM PUERTO INNER JOIN
    PAIS ON PUERTO.PA_CODIGO = PAIS.PA_CODIGO
WHERE PUERTO.PU_TIPO = 'M'







































































GO
