CREATE VIEW dbo.[VINFOMASINGEN]
with encryption as
SELECT COUNT(MAESTRO.MA_CODIGO) AS COUNTMA_CODIGO, 
    TIPO.TI_NOMBRE
FROM MAESTRO LEFT OUTER JOIN
    TIPO ON 
    MAESTRO.TI_CODIGO = TIPO.TI_CODIGO LEFT OUTER JOIN
    CONFIGURATIPO ON 
    MAESTRO.TI_CODIGO = CONFIGURATIPO.TI_CODIGO
GROUP BY CONFIGURATIPO.CFT_TIPO, MAESTRO.TI_CODIGO, 
    TIPO.TI_NOMBRE, MAESTRO.MA_GENERICO, 
    MAESTRO.MA_INV_GEN
HAVING (CONFIGURATIPO.CFT_TIPO = 'R' OR
    CONFIGURATIPO.CFT_TIPO = 'E' OR
    CONFIGURATIPO.CFT_TIPO = 'C' OR
    CONFIGURATIPO.CFT_TIPO = 'H' OR
    CONFIGURATIPO.CFT_TIPO = 'Q') AND 
    (MAESTRO.MA_GENERICO = 0 OR
    MAESTRO.MA_GENERICO IS NULL) AND 
    (MAESTRO.MA_INV_GEN = 'I')






































































GO
