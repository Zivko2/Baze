CREATE VIEW dbo.VIMP_EXP
with encryption as
SELECT     dbo.PEDIMPDET.MA_CODIGO, dbo.PEDIMPDET.TI_CODIGO, dbo.PEDIMPDET.ME_CODIGO, dbo.PEDIMPDET.PID_NOMBRE AS TIM_NOMBRE, 
                      dbo.PEDIMPDET.PID_NAME AS TIM_NAME, dbo.PEDIMPDET.PID_NOPARTE AS TIM_NOPARTE, dbo.PEDIMPDET.PID_COS_UNI AS TIM_COS_UNI, 
                      dbo.PEDIMPDET.PID_CANT AS TIM_CANT, dbo.VPEDIMP.PI_FEC_PAG AS TIM_FECHA
FROM         dbo.PEDIMPDET LEFT OUTER JOIN
                      dbo.FACTIMP ON dbo.PEDIMPDET.PI_CODIGO = dbo.FACTIMP.PI_CODIGO RIGHT OUTER JOIN
                      dbo.VPEDIMP ON dbo.PEDIMPDET.PI_CODIGO = dbo.VPEDIMP.PI_CODIGO

































































GO
