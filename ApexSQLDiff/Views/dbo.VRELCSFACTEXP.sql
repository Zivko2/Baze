CREATE VIEW dbo.VRELCSFACTEXP
with encryption as
SELECT     dbo.ENTRYSUM.ET_CODIGO, dbo.ENTRYSUM.ET_FEC_ENTRYS, dbo.COSTSUBPER.CS_CODIGO, dbo.FACTEXP.FE_CODIGO
FROM         dbo.FACTEXP INNER JOIN
                      dbo.COSTSUBPER ON dbo.FACTEXP.CL_DESTINI = dbo.COSTSUBPER.CL_CODIGO FULL OUTER JOIN
                      dbo.ENTRYSUM ON dbo.FACTEXP.ET_CODIGO = dbo.ENTRYSUM.ET_CODIGO AND 
                      dbo.COSTSUBPER.CS_FECHAINI <= dbo.ENTRYSUM.ET_FEC_ENTRYS AND 
                      dbo.COSTSUBPER.CS_FECHAFIN >= dbo.ENTRYSUM.ET_FEC_ENTRYS
WHERE     (dbo.ENTRYSUM.ET_FEC_ENTRYS IS NOT NULL)
GROUP BY dbo.ENTRYSUM.ET_CODIGO, dbo.ENTRYSUM.ET_FEC_ENTRYS, dbo.COSTSUBPER.CS_CODIGO, dbo.FACTEXP.FE_CODIGO










GO
