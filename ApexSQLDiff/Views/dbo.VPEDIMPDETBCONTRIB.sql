CREATE VIEW dbo.VPEDIMPDETBCONTRIB
with encryption as

SELECT PIB_INDICEB, ISNULL((SELECT PIB_CONTRIBTOTMN FROM PEDIMPDETBCONTRIBUCION WHERE CON_CODIGO=3 AND PEDIMPDETBCONTRIBUCION.PIB_INDICEB=PEDIMPDETB.PIB_INDICEB),0) AS IVA,
ISNULL((SELECT PIB_CONTRIBPOR FROM PEDIMPDETBCONTRIBUCION WHERE CON_CODIGO=3 AND PEDIMPDETBCONTRIBUCION.PIB_INDICEB=PEDIMPDETB.PIB_INDICEB),0) AS BASEIVA,
ISNULL((SELECT SUM(PIB_CONTRIBTOTMN) FROM PEDIMPDETBCONTRIBUCION WHERE CON_CODIGO=6 AND PEDIMPDETBCONTRIBUCION.PIB_INDICEB=PEDIMPDETB.PIB_INDICEB),0) AS IGI
FROM PEDIMPDETB






























