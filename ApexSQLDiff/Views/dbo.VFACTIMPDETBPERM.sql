CREATE VIEW dbo.VFACTIMPDETBPERM
with encryption as
SELECT     dbo.FACTIMPDET.FI_CODIGO, dbo.FACTIMPDET.AR_IMPMX, dbo.FACTIMPPERM.FI_CODIGO AS Expr1, dbo.FACTIMPPERM.PE_CODIGO
FROM         dbo.FACTIMPDET LEFT OUTER JOIN
                      dbo.FACTIMPPERM ON dbo.FACTIMPDET.FID_INDICED = dbo.FACTIMPPERM.FID_INDICED
GROUP BY dbo.FACTIMPDET.FI_CODIGO, dbo.FACTIMPDET.AR_IMPMX, dbo.FACTIMPPERM.FI_CODIGO, dbo.FACTIMPPERM.PE_CODIGO


































































