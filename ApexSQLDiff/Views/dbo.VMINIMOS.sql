CREATE VIEW dbo.VMINIMOS
with encryption as
SELECT     dbo.MAESTRO.MA_NOPARTE, dbo.MAESTRO.MA_NOMBRE, dbo.MAESTRO.MA_NAME, dbo.ALMACEN.ALM_NOMBRE, 
                      dbo.MAESTROALMDET.MAM_MINIMOALM, dbo.VINVENTARIOEXITESUM.END_SALDOALM, dbo.MEDIDA.ME_CORTO
FROM         dbo.MEDIDA RIGHT OUTER JOIN
                      dbo.MAESTRO MAESTRO_1 ON dbo.MEDIDA.ME_CODIGO = MAESTRO_1.ME_COM RIGHT OUTER JOIN
                      dbo.MAESTROALMDET INNER JOIN
                      dbo.MAESTRO ON dbo.MAESTROALMDET.MA_CODIGO = dbo.MAESTRO.MA_CODIGO INNER JOIN
                      dbo.VINVENTARIOEXITESUM ON dbo.MAESTROALMDET.MA_CODIGO = dbo.VINVENTARIOEXITESUM.MA_CODIGO AND 
                      dbo.MAESTROALMDET.ALM_CODIGO = dbo.VINVENTARIOEXITESUM.ALM_DESTINO AND 
                      dbo.MAESTROALMDET.MAM_MINIMOALM >= dbo.VINVENTARIOEXITESUM.END_SALDOALM INNER JOIN
                      dbo.ALMACEN ON dbo.MAESTROALMDET.ALM_CODIGO = dbo.ALMACEN.ALM_CODIGO ON MAESTRO_1.MA_CODIGO = dbo.MAESTRO.MA_GENERICO


GO
