CREATE TRIGGER dbo.[DELETE_COMMINVDET] ON [dbo].[COMMINVDET] 
FOR DELETE 
AS
declare @fed_indiced int, @IVD_CANT decimal(38,6)
	select @IVD_CANT=IVD_CANT, @fed_indiced=fed_indiced from deleted


	if exists (select * from factexpdet where FED_INDICED=@fed_indiced)
	update factexpdet
	set fed_saldo=isnull(fed_saldo,0)+@IVD_CANT
	where FED_INDICED=@fed_indiced

































































GO
