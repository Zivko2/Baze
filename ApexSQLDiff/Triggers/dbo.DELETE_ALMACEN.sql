CREATE TRIGGER dbo.[DELETE_ALMACEN] ON [dbo].[ALMACEN] 
FOR  DELETE 
AS
declare @alm_codigo int


	select @alm_codigo=alm_codigo from deleted

	if exists (select * from almacendet where alm_codigo =@alm_codigo)
	delete from almacendet where alm_codigo=@alm_codigo




























































